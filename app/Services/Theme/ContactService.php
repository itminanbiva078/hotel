<?php

namespace App\Services\Theme;
use App\Repositories\Theme\ContactRepositories;

class ContactService
{

    /**
     * @var ContactRepositories
     */
    private $systemRepositories;
    /**
     * ContactService constructor.
     * @param ContactRepositories $ContactRepositories
     */
    public function __construct(ContactRepositories $systemRepositories)
    {
        $this->systemRepositories = $systemRepositories;
    }

    /**
     * @param $request
     * @return mixed
     */
    public function getList($request)
    {
        return $this->systemRepositories->getList($request);
    }
   
    /**
     * @param $request
     * @return \App\Models\Sales
     */
    public function store($request)
    {
        return $this->systemRepositories->store($request);
    }

     
   
    /**
     * @param $request
     * @return \App\Models\Sales
     */
    public function details($id)
    {

        return $this->systemRepositories->details($id);
    }

    /**
     * @param $request
     * @param $id
     */
    public function destroy($id)
    {
        return $this->systemRepositories->destroy($id);
    }
}